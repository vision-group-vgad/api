/**
 * filterRecords
 * filters:
 *   source: string
 *   status: string (Applied, Interviewed, Offered, Rejected, etc)
 *   position: string (partial match)
 *   dateFrom: ISO date string
 *   dateTo: ISO date string
 */
export function filterRecords(filters = {}) {
  const { source, status, position, dateFrom, dateTo } = filters;
  return recruitmentFunnelData.filter((r) => {
    if (source && r.recruitmentSource?.toLowerCase() !== source.toLowerCase())
      return false;
    if (status && r.status?.toLowerCase() !== status.toLowerCase()) return false;
    if (
      position &&
      (!r.position || !r.position.toLowerCase().includes(position.toLowerCase()))
    )
      return false;
    if (dateFrom) {
      const from = new Date(dateFrom);
      const applied = new Date(r.applicationDate);
      if (applied < from) return false;
    }
    if (dateTo) {
      const to = new Date(dateTo);
      const applied = new Date(r.applicationDate);
      if (applied > to) return false;
    }
    return true;
  });
}

/**
 * getFunnelData
 * returns funnel stages and counts (also conversion rates)
 * filters same as filterRecords
 */
export function getFunnelData(filters = {}) {
  const records = filterRecords(filters);
  const totalApplications = records.length;

  const screened = records.filter((r) =>
    String(r.screeningResult || "").toLowerCase() === "pass"
  ).length;

  // assessment considered passed when result is 'Good' or 'Excellent'
  const assessed = records.filter((r) =>
    ["good", "excellent"].includes(String(r.assessmentResult || "").toLowerCase())
  ).length;

  // interviewed: those who have interviewDate and interviewOutcome not 'N/A' or null
  const interviewed = records.filter(
    (r) => r.interviewDate && String(r.interviewOutcome || "").toLowerCase() !== "n/a"
  ).length;

  // offers: offerStatus 'Offered' or 'Accepted'
  const offers = records.filter((r) =>
    ["offered", "accepted"].includes(String(r.offerStatus || "").toLowerCase())
  ).length;

  // hires: those with hireDate (non-null) or offerStatus accepted with hireDate
  const hires = records.filter((r) => r.hireDate !== null).length;

  // Prepare stage list
  const stages = [
    { stage: "Applications", count: totalApplications },
    { stage: "Screened (Passed)", count: screened },
    { stage: "Assessed (Passed)", count: assessed },
    { stage: "Interviewed", count: interviewed },
    { stage: "Offers (Made)", count: offers },
    { stage: "Hires", count: hires }
  ];

  // Add conversion rates (from previous stage)
  const stagesWithConversion = stages.map((s, idx) => {
    const prev = idx === 0 ? null : stages[idx - 1].count;
    const conversion =
      prev === null || prev === 0 ? null : Number(((s.count / prev) * 100).toFixed(1));
    return { ...s, conversionRateFromPreviousPct: conversion };
  });

  return {
    filtersApplied: filters,
    stages: stagesWithConversion
  };
}
